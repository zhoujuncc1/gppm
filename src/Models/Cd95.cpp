const double Model::p[Model::N_PARAMS] = { 1, 0.00012772, 0.66933, 1e-05, 0.00059466, 1, 0.88751, 0.00080444,
                  0.0022498, 0.12053, 0.028915, 0.15029, 0.00072043, 0.35882, 3.6842, 0.022299, 0.0064182};


void Model::odefun(const state_type &x, state_type &dxdt, const double t) {
	dxdt[0] = -(1*p[0]*x[0]*x[15]) ;
	dxdt[1] = +(1*p[0]*x[0]*x[15]) -(1*p[1]*x[1]*x[7]) -(1*p[2]*x[1]*x[16]) -(1*p[3]*x[1]*x[17]) ;
	dxdt[2] = +(1*p[1]*x[1]*x[7]) -(1*p[4]*x[2]*x[7]) -(1*p[5]*x[2]*x[16]) -(1*p[6]*x[2]*x[17]) ;
	dxdt[3] = +(1*p[2]*x[1]*x[16]) -(1*p[4]*x[3]*x[7]) -(1*p[5]*x[3]*x[16]) -(1*p[6]*x[3]*x[17]) ;
	dxdt[4] = +(1*p[3]*x[1]*x[17]) -(1*p[4]*x[4]*x[7]) -(1*p[5]*x[4]*x[16]) -(1*p[6]*x[4]*x[17]) ;
	dxdt[5] = +(1*p[4]*x[2]*x[7]) +(1*p[4]*x[2]*x[7]) -(1*p[7]*x[5]*x[5]) -(1*p[7]*x[5]*x[5]) +(1*p[9]*x[7]*x[9]) ;
	dxdt[6] = -(1*p[8]*x[6]*x[8]) ;
	dxdt[7] = -(1*p[1]*x[1]*x[7]) -(1*p[4]*x[2]*x[7]) -(1*p[4]*x[3]*x[7]) -(1*p[4]*x[4]*x[7]) -(1*p[9]*x[7]*x[9]) ;
	dxdt[8] = +(1*p[7]*x[5]*x[5]) -(1*p[8]*x[6]*x[8]) +(1*p[8]*x[6]*x[8]) -(1*p[10]*x[8]) ;
	dxdt[9] = +(1*p[8]*x[6]*x[8]) -(1*p[9]*x[7]*x[9]) +(1*p[9]*x[7]*x[9]) -(1*p[11]*x[9]) ;
	dxdt[10] = +(1*p[5]*x[2]*x[16]) +(1*p[4]*x[3]*x[7]) -(1*p[12]*x[10]*x[18]) ;
	dxdt[11] = -(1*p[13]*x[11]*x[13]) ;
	dxdt[12] = +(1*p[13]*x[11]*x[13]) -(1*p[14]*x[12]) ;
	dxdt[13] = +(1*p[12]*x[10]*x[18]) -(1*p[13]*x[11]*x[13]) +(1*p[13]*x[11]*x[13]) -(1*p[15]*x[13]) ;
	dxdt[14] = +(1*p[14]*x[12]) -(1*p[16]*x[14]) ;
	dxdt[15] = -(1*p[0]*x[0]*x[15]) ;
	dxdt[16] = -(1*p[2]*x[1]*x[16]) -(1*p[5]*x[2]*x[16]) -(1*p[5]*x[3]*x[16]) -(1*p[5]*x[4]*x[16]) ;
	dxdt[17] = -(1*p[3]*x[1]*x[17]) -(1*p[6]*x[2]*x[17]) -(1*p[6]*x[3]*x[17]) -(1*p[6]*x[4]*x[17]) ;
	dxdt[18] = -(1*p[12]*x[10]*x[18]) ;
	dxdt[19] = +(1*p[6]*x[2]*x[17]) +(1*p[4]*x[4]*x[7]) ;
	dxdt[20] = +(1*p[5]*x[3]*x[16]) ;
	dxdt[21] = +(1*p[6]*x[3]*x[17]) +(1*p[5]*x[4]*x[16]) ;
	dxdt[22] = +(1*p[6]*x[4]*x[17]) ;
}

